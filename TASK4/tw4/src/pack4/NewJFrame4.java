    public void DrawObject(){
        Graphics g = jPanel1.getGraphics();
        g.setColor(Color.BLACK);
        g.fillRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
        for(int i=0 ; i<9 ; i++){
            int x1 = Vertices[i][0] + cX;
            int y1 = cY - Vertices[i][1];
            g.setColor(Color.WHITE);
            g.fillOval(x1-4, y1-4, 8, 8); //size of object
            g.setColor(Color.BLUE);
            g.drawString(String.valueOf(i+1), x1-3, y1-6);
            int x2 , y2;
            if( i < 8){
              x2 = Vertices[i+1][0] + cX;
              y2 = cY - Vertices[i+1][1];
            }else{
                x2 = Vertices[0][0] + cX;
                y2 = cY - Vertices[0][1];
            }
            g.drawLine(x1, y1, x2, y2);
        }
    }
    
