    public static int[][] Vertices = new int[9][2];  //4 vertices and 2 dimensions x and y
    public static int cX , cY;
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Random val = new Random();
        cX = jPanel1.getWidth() / 2;
        cY = jPanel1.getHeight() / 2;
        for(int i = 0 ; i<9 ; i++){
            Vertices[i][0] = cX - val.nextInt(jPanel1.getWidth());  //object cant be bigger than panel
            Vertices[i][1] = cY - val.nextInt(jPanel1.getHeight()); //object cant be bigger than panel
        }
        DrawObject();
    }//GEN-LAST:event_jButton1ActionPerformed

