    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
               double alfa = Math.toRadians(Double.parseDouble(jTextField1.getText()));
        int n=9; 
        if(jRadioButton1.isSelected()) n=0;
        if(jRadioButton2.isSelected()) n=1;
        if(jRadioButton3.isSelected()) n=2;
        if(jRadioButton4.isSelected()) n=3;
        if(jRadioButton5.isSelected()) n=4;
        if(jRadioButton6.isSelected()) n=5;
        if(jRadioButton7.isSelected()) n=6;
        if(jRadioButton8.isSelected()) n=7;
        if(jRadioButton9.isSelected()) n=8;
        
        for (int i=0;i<9;i++){
            double xn = Math.cos(alfa)*(Vertices[i][0]-Vertices[n][0])  - Math.sin(alfa)*(Vertices[i][1]-Vertices[n][1]); 
            double yn = Math.sin(alfa)*(Vertices[i][0]-Vertices[n][0])  + Math.cos(alfa)*(Vertices[i][1]-Vertices[n][1]);
            Vertices[i][0] = (int)Math.round(xn) + Vertices[n][0];
            Vertices[i][1] = (int)Math.round(yn) + Vertices[n][1];
        
        }
        DrawObject();

    }//GEN-LAST:event_jButton2ActionPerformed

